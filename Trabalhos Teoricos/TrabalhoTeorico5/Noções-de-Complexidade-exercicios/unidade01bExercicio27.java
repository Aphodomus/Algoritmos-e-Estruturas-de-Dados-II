/* Faça um método que receba um número inteiro n e efetue o número de
subtrações pedido em:  2n^3 + 5*/

public class unidade01bExercicio27 {
    public static void main(String[] args) {
        i = 0;

        for (i = 0; i < n; i ++) {
            for (j = 0; j < n; j++) {
                for (k = 0; k < n; k++) {
                    a--;
                    b--;  
                }
            }
        }

        a--;
        b--;
        c--;
        d--;
        e--;
    }
}

