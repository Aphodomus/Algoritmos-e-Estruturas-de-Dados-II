/* Faça um método que receba um número inteiro n e efetue o número de
subtrações pedido em:  log2(n) + 5log2(n)*/

public class unidade01bExercicio29 {
    public static void main(String[] args) {
        int i = 0;
        for (i = 1; i < n; i *= 2) {
            a--;
        }

        for (i = 1; i < n; i *= 2) {
            a--;
            b--;
            c--;
            d--;
            e--;
        }
    }
}

